namespace NovaEngine.IO;

/// <summary>The buttons a mouse can have and be detected.</summary>
public enum MouseButton
{
    /// <summary>The left mouse button.</summary>
    LeftButton,

    /// <summary>The middle mouse button (scroll wheel button).</summary>
    MiddleButton,

    /// <summary>The right mouse button.</summary>
    RightButton,

    /// <summary>The 4th mouse button (typically the 'back' button).</summary>
    BackButton,

    /// <summary>The 5th mouse button (typically the 'forward' button).</summary>
    ForwardButton
}
